public class Operaciones {

    public void realizarSuma(int op1, int op2){
        int suma = op1+op2;
        System.out.println("El resultado de la suma es "+suma);
    }

    public void realizarModulo(int op1){
        int modulo = op1%5;
        System.out.println("El modulo del numero con 5 es "+modulo);
    }
}
